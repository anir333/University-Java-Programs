package kdg.be;

public class ex03_07_Hexadecimal {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            System.out.print(i + " ");
        }
        for (int i = 0; i < 6; i++) {
            char letter = 'A';
            System.out.print((char) (letter+i) + " ");
        }
    }
}
